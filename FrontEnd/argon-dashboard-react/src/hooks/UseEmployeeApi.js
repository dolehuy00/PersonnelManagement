
import { useState, useEffect } from 'react';
import {
    filterEmployee,
    addEmployee,
    getOneEmployee,
    editEmployee,
    lockEmployee,
    unlockEmployee,
    searchEmployee,
    getInfoByEmployee,
    searchEmployeeByLeader,
    changeImageEmployeeByAdmin,
    changeImageEmployeeByUser
} from 'services/management/EmployeeApi.js';
import { useNavigate } from "react-router-dom";

export const useFilterEmployee = (dataFilter, sortBy, page, pageSize) => {
    const [data, setData] = useState([]);
    const [loading, setLoading] = useState(true);
    const [error, setError] = useState(null);
    const navigate = useNavigate();

    useEffect(() => {
        const getData = async () => {
            setLoading(true);
            try {
                const result = await filterEmployee(dataFilter, sortBy, page, pageSize);
                setData(result);
                setLoading(false);
            } catch (error) {
                if (error.response.status === 401) {
                    navigate('/auth');
                } else {
                    setLoading(false);
                    setError(error);
                }
            }
        };
        getData();
    }, [dataFilter, sortBy, page, pageSize, navigate]);

    return { data, loading, error };
};

